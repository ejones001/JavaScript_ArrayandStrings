// Initial array containing some elements
let fruits = ['apple', 'banana', 'orange'];

// Adding a new element to the end of the array using push method
fruits.push('grape');

console.log('After adding grape:', fruits); // Output: ['apple', 'banana', 'orange', 'grape']

// Removing the last element from the array using pop method
let removedFruit = fruits.pop();

console.log('Removed fruit:', removedFruit); // Output: grape
console.log('After removing last element:', fruits); // Output: ['apple', 'banana', 'orange']
